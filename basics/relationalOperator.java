package basics;

public class relationalOperator {
    static void main() {
        int a = 1;
        int b = 2;
        int c = 1;
        System.out.println(a<b);
        System.out.println(a>c);
        System.out.println(a>=c);
        System.out.println(a<=c);
        System.out.println(a!=c);
        System.out.println(a==c);
    }
}
